    private void criaTabela() {
        
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            };
        };
        
        MunicipioDao dao = new MunicipioDao();
        
        jTableMunicipios.setModel(modelo);
        
        modelo.addColumn("ID");
        modelo.addColumn("NOME");
        modelo.addColumn("IBGE");
        
        jTableMunicipios.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableMunicipios.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableMunicipios.getColumnModel().getColumn(2).setPreferredWidth(200);
        
        try {
            for (Municipio municipio : dao.getAll()) {
                modelo.addRow(new Object[]{municipio.getId(), municipio.getNome(), municipio.getIbge()});
            }
        } catch (BancoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
     
     public Municipio getLinha() throws BancoException {
       
        int selecionada = jTableMunicipios.getSelectedRow();
        
        Municipio municipio = new Municipio();
        MunicipioDao dao = new MunicipioDao();

        if (selecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um municipio!");
        }

        Object obj = jTableMunicipios.getValueAt(selecionada, 0);
        int codigo = (int) obj;

        municipio = dao.getMunicipio(codigo);

        return municipio;

    }
    
