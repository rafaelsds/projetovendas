    private void criaTabela() {
        
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            };
        };
        
        MetaDao dao = new MetaDao();
        
        jTableMetas.setModel(modelo);
        
        modelo.addColumn("ID");
        modelo.addColumn("NOME");
        modelo.addColumn("VALOR");
        modelo.addColumn("INÍCIO");
        modelo.addColumn("FINAL");
        
        jTableMetas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableMetas.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableMetas.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTableMetas.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTableMetas.getColumnModel().getColumn(4).setPreferredWidth(200);
        
        try {
            for (Meta meta : dao.getAll()) {
                modelo.addRow(new Object[]{meta.getId(), meta.getDescricao(), meta.getValor(), meta.getDataInicio(), meta.getDataFinal()});
            }
        } catch (BancoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
   
    
    public Meta getLinha() throws BancoException {
       
        int selecionada = jTableMetas.getSelectedRow();
        
        Meta meta = new Meta();
        MetaDao dao = new MetaDao();

        Object obj = jTableMetas.getValueAt(selecionada, 0);
        int codigo = (int) obj;

        meta = dao.getMeta(codigo);

        return meta;

    }
    
