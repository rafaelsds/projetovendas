package model;

public class Cep {
    
    private Integer id;
    private String cep;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
    
    public void setCep(String cep) {
        this.cep = cep;
    }
    
    public String getCep() {
        return this.cep;
    }
}
